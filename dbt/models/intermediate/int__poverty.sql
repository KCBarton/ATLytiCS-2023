SELECT

  TWENTY_ONE.COC_NAME
  , TWENTY_ONE.AREA

  , STRUCT(
    TEN.UNDER_18_POVERTY          AS YEAR_10    
    , ELEVEN.UNDER_18_POVERTY     AS YEAR_11
    , TWELVE.UNDER_18_POVERTY     AS YEAR_12
    , THIRTEEN.UNDER_18_POVERTY   AS YEAR_13
    , FOURTEEN.UNDER_18_POVERTY   AS YEAR_14
    , FIFTEEN.UNDER_18_POVERTY    AS YEAR_15
    , SIXTEEN.UNDER_18_POVERTY    AS YEAR_16
    , SEVENTEEN.UNDER_18_POVERTY  AS YEAR_17
    , EIGHTEEN.UNDER_18_POVERTY   AS YEAR_18
    , NINETEEN.UNDER_18_POVERTY   AS YEAR_19
    , TWENTY.UNDER_18_POVERTY     AS YEAR_20
    , TWENTY_ONE.UNDER_18_POVERTY AS YEAR_21
  ) AS UNDER_18_POVERTY

FROM            {{ ref('stg__pov_10') }} TEN
FULL OUTER JOIN {{ ref('stg__pov_11') }} ELEVEN     ON TEN.COC_NAME = ELEVEN.COC_NAME     
FULL OUTER JOIN {{ ref('stg__pov_12') }} TWELVE     ON TEN.COC_NAME = TWELVE.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_13') }} THIRTEEN   ON TEN.COC_NAME = THIRTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_14') }} FOURTEEN   ON TEN.COC_NAME = FOURTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_15') }} FIFTEEN    ON TEN.COC_NAME = FIFTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_16') }} SIXTEEN    ON TEN.COC_NAME = SIXTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_17') }} SEVENTEEN  ON TEN.COC_NAME = SEVENTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_18') }} EIGHTEEN   ON TEN.COC_NAME = EIGHTEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_19') }} NINETEEN   ON TEN.COC_NAME = NINETEEN.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_20') }} TWENTY     ON TEN.COC_NAME = TWENTY.COC_NAME
FULL OUTER JOIN {{ ref('stg__pov_21') }} TWENTY_ONE ON TEN.COC_NAME = TWENTY_ONE.COC_NAME